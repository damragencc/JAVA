package day07_StringManipilationss;

public class C06_Replace {
    public static void main(String[] args) {

        // CharSequence : char serisi, yani charların yan yana gelip birlesmesi demektir.
        // charları birleştirince string olur.

        String str = " Java Candir";

        System.out.println(str.replace('a','A' )); // JAvA CAndir

        System.out.println(str.replace("a", "A")); // JAvA CAndir

        System.out.println();

        System.out.println();
        System.out.println();
        System.out.println();


    }
}
